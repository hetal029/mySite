<?php

namespace App\Models\NoteCategories\Traits\Attribute;

/**
 * Class BlogCategoryAttribute.
 */
trait NoteCategoryAttribute
{
    /**
     * @return string
     */
    public function getActionButtonsAttribute()
    {
        return '<div class="btn-group action-btn">
                    '.$this->getEditButtonAttribute('edit-note-category', 'admin.noteCategories.edit').'
                    '.$this->getDeleteButtonAttribute('delete-note-category', 'admin.noteCategories.destroy').'
                </div>';
    }

    /**
     * @return string
     */
    public function getStatusLabelAttribute()
    {
        if ($this->isActive()) {
            return "<label class='label label-success'>".trans('labels.general.active').'</label>';
        }

        return "<label class='label label-danger'>".trans('labels.general.inactive').'</label>';
    }

    /**
     * @return bool
     */
    public function isActive()
    {
        return $this->status == 1;
    }
}
